package com.example.kintai.syain;

public class Bumon{
	private String code;
	private String meisyo;
	private String ryakusyo;
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getMeisyo() {
		return meisyo;
	}
	public void setMeisyo(String meisyo) {
		this.meisyo = meisyo;
	}
	public String getRyakusyo() {
		return ryakusyo;
	}
	public void setRyakusyo(String ryakusyo) {
		this.ryakusyo = ryakusyo;
	}

}